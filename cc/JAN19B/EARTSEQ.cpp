int a[50005],pri[1000005],p[1000005],ps;
void init_prime(int n){
	for(int i=2;i<=n;++i){
		if(!p[i]){
			pri[++ps]=i;
		}
		for(int j=1;j<=ps && i*pri[j]<=n;++j){
			p[i*pri[j]]=1;
			if(!(i%pri[j]))break;
		}
	}
}
